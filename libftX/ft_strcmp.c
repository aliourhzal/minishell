#include "libft.h"

int	ft_strcmp(char *s1, char *s2)
{
	int	i;
	int	out;

	i = 0;
	while (s1[i] || s2[i])
	{
		if ((s1[i] < s2[i]) || (s1[i] > s2[i]))
		{
			out = s1[i] - s2[i];
			return (out);
		}
		i++;
	}
	return (0);
}
